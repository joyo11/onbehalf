const US_STATE_ABBR = new Set(US_STATES.map((s) => s.code));

/**
 * Best-effort: try to derive country/state from a free-text "City, ST" or
 * "City, ST, USA" string. Returns nulls if we can't parse cleanly.
 */
export function guessCountryStateFrom(text: string | null | undefined): {
  country: string | null;
  state: string | null;
  city: string | null;
} {
  if (!text) return { country: null, state: null, city: null };
  const parts = text.split(",").map((p) => p.trim());
  if (parts.length >= 2) {
    const last = parts[parts.length - 1].toUpperCase();
    if (last === "USA" || last === "US" || last === "UNITED STATES") {
      const maybeState = parts[parts.length - 2].toUpperCase();
      if (US_STATE_ABBR.has(maybeState)) {
        return { country: "US", state: maybeState, city: parts.slice(0, -2).join(", ") || null };
      }
    }
    const last2 = parts[parts.length - 1].toUpperCase();
    if (US_STATE_ABBR.has(last2)) {
      return { country: "US", state: last2, city: parts.slice(0, -1).join(", ") || null };
    }
  }
  return { country: null, state: null, city: null };
}

export function formatLocation(country: string, state: string, city: string): string {
  const countryName = COUNTRIES.find((c) => c.code === country)?.name ?? country;
  if (country === "US") {
    const stateName = US_STATES.find((s) => s.code === state)?.name ?? state;
    return [city, stateName, "USA"].filter(Boolean).join(", ");
  }
  return [city, countryName].filter(Boolean).join(", ");
}
